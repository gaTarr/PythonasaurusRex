def in_dict(a_dict, a_value):
    """ This accepts a dictionary, and a value to search for
    :param a_dict: is the dictionary that will be searched
    :param a_value is the value that be searched for
    :returns True if the value is in the dictionary, false if it is not"""
    return a_value in a_dict


